-- Mise à jour ds temps restants estimés sur les tâches de production
update tp1
set DureeRestanteEstimee = tp1.DureePrevue - (
	select SUM(w.Heures)
	from jo.TacheProd tp
	inner join jo.Tache t on tp.IdTache = t.IdTache
	inner join jo.Travail w on w.IdTache = t.IdTache
	where tp.IdTache = tp1.IdTache
	group by tp.IdTache
)
from jo.TacheProd tp1
	inner join jo.Tache t on tp1.IdTache = t.IdTache
	inner join jo.Travail w on w.IdTache = t.IdTache

update jo.TacheProd set DureeRestanteEstimee = 0 where DureeRestanteEstimee < 0
go
